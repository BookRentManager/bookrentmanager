export function CountrySelect({ 
  value, 
  onChange, 
  placeholder = "Select country", 
  disabled = false 
}: CountrySelectProps) {
  const [open, setOpen] = useState(false);
  const isMobile = useIsMobile();

  const triggerButton = (
    <Button
      type="button"
      variant="outline"
      role="combobox"
      aria-expanded={open}
      className="w-full justify-between h-11"
      disabled={disabled}
    >
      <span className={cn(!value && "text-muted-foreground")}>
        {value || placeholder}
      </span>
      <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
    </Button>
  );

  const commandContent = (
    <Command>
      <CommandInput 
        placeholder="Search country..." 
        className={cn(isMobile ? "h-14 text-base" : "h-11")} 
        autoFocus={isMobile}
      />
      <CommandList 
        className={cn(isMobile && "max-h-[60vh]")}
        style={isMobile ? { touchAction: 'manipulation' } : undefined}
      >
        <CommandEmpty>No country found.</CommandEmpty>
        <CommandGroup>
          {countries.map((country) => (
            <CommandItem
              key={country.code}
              value={country.name}
              onSelect={(currentValue) => {
                onChange(currentValue === value ? "" : currentValue);
                setOpen(false);
              }}
              onPointerDown={(e) => {
                // On mobile, handle selection immediately when pointer goes down
                // This bypasses iOS keyboard blocking behavior
                if (isMobile) {
                  e.preventDefault();
                  onChange(country.name === value ? "" : country.name);
                  setOpen(false);
                }
              }}
              className={cn(
                "cursor-pointer",
                isMobile && "py-3 min-h-[44px] text-base"
              )}
            >
              <Check
                className={cn(
                  "mr-2 h-4 w-4",
                  value === country.name ? "opacity-100" : "opacity-0"
                )}
              />
              {country.name}
            </CommandItem>
          ))}
        </CommandGroup>
      </CommandList>
    </Command>
  );

  if (isMobile) {
    return (
      <Drawer open={open} onOpenChange={setOpen}>
        <DrawerTrigger asChild>
          {triggerButton}
        </DrawerTrigger>
        <DrawerContent>
          <DrawerHeader className="text-left">
            <DrawerTitle>Select Country</DrawerTitle>
          </DrawerHeader>
          <div className="px-4 pb-6">
            {commandContent}
          </div>
        </DrawerContent>
      </Drawer>
    );
  }

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        {triggerButton}
      </PopoverTrigger>
      <PopoverContent className="w-full p-0" align="start">
        {commandContent}
      </PopoverContent>
    </Popover>
  );
}
